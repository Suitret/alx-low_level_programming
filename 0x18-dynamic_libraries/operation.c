#include "main.h"
#include <stdio.h>

/**
 * add - performs addition
 * @a: first value
 * @b: second value
 * Return: a + b
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - performs substraction
 * @a: first value
 * @b: second value
 * Return: a - b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - performs multiplication
 * @a: first value
 * @b: second value
 * Return: a * b
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - performs division
 * @a: first value
 * @b: second value
 * Return: a / b
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - performs modulo
 * @a: first value
 * @b: second value
 * Return: a % b
 */

int mod(int a, int b)
{
	return (a % b);
}
